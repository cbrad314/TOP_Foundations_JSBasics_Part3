function add7(addend){
    return(addend + 7);
}

function multiply (operand1,operand2){
    return(operand1*operand2);
}

function capitalize (capitalizeInput) { 
    return capitalizeInput.charAt(0).toUpperCase()+capitalizeInput.substring(1).toLowerCase();
}

function lastLetter (lastLetterInput){
    return(lastLetterInput.substring(lastLetterInput.length-1));
}
